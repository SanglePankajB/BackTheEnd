// math.js

// Function to add two numbers
function add(a, b) {
    return a + b;
  }
  
  // Function to subtract two numbers
  function subtract(a, b) {
    return a - b;
  }
  
  // Export the functions so they can be used in other modules (if needed)
  module.exports = {
    add,
    subtract,
  };
  